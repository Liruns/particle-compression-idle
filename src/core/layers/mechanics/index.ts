/**
 * layers/mechanics — 층별 메커니즘 모듈. (tech-architecture.md §1.1·§3.4·§4.4, systems.md §2)
 *
 * 각 메커니즘 모듈은 **자기완결**(§4.4): 직렬화·방치기본값·이벤트 발행을 스스로 책임 →
 *   세이브/오프라인/루프 코어가 메커니즘 내부를 모른다. "한 층=한 새로움"을 모듈 추가만으로 확장.
 *
 * 모든 메커니즘이 구현해야 하는 공통 인터페이스(아래 LayerMechanic):
 *  - serialize()/deserialize(): 복잡 상태(스핀 네트워크 그래프 등)의 직렬화 책임(§1.1, R7).
 *    세이브 시스템은 이 결과(평범한 JSON 값)만 봉투에 담는다.
 *  - getIdleBaselineMultiplier(): 오프라인 중 "방치 기본 기여"(§3.4). 오프라인 계산기는
 *    메커니즘별 공식을 모르고 이 값만 질의(역전된 의존).
 *
 * 메커니즘 목록(systems §2): 오비탈 공명(원자)·핵력 게이지(핵)·색전하(핵자)·점근자유(쿼크)
 *  ·위상 겹침(프리온)·진동 하모닉스(끈)·스핀 네트워크(루프)·불확정 요동(거품)
 *  ·홀로그래픽(정보)·픽셀 붕괴(플랑크).
 *
 * TODO(M1.4): 오비탈 공명(첫 능동 손맛, 원자층). TODO(M1.6): 위상 겹침(프리온).
 */

/** 모든 층 메커니즘이 구현하는 공통 계약(§1.1·§3.4). */
export interface LayerMechanic {
  /** 메커니즘 식별자(층 slug 등). */
  readonly id: string;
  /** 복잡 상태 직렬화 → 평문 JSON 값(§1.1 책임 위임, R7). */
  serialize(): unknown;
  /** 평문 JSON 값에서 상태 복원. 누락/손상 시 기본값(§1.3). */
  deserialize(data: unknown): void;
  /** 오프라인 중 방치 기본 기여 배율(§3.4, 양수 장기평균). */
  getIdleBaselineMultiplier(): number;
}

export {}; // 구체 메커니즘 모듈은 후속 마일스톤에서 이 인터페이스로 추가.
